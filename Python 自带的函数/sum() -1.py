numbers = [1, 2, 3, 4, 5]
total = sum(numbers)
print(total)  # 输出: 15

data = (10, 20, 30, 40)
total = sum(data)
print(total)  # 输出: 100

r = range(1, 6) # 表示 1 到 5 的整数序列
total = sum(r)
print(total)  # 输出: 15

numbers = [1, 2, 3]
start_value = 10
total = sum(numbers, start_value)
print(total)  # 输出: 16 (10 + 1 + 2 + 3)
